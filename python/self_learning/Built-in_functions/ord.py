# Ord() fonksiyonunu kullanarak bir karakterin Unicode kod noktasını bulun

# Öncelikle karakteri tanımlayalım
character = "A"

# Karakterin Unicode kod noktasını bulalım
code_point = ord(character)

# Kod noktasını ekrana yazdıralım
print(code_point)


