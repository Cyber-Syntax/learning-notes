# This example will show how to use the built-in clear() method to clear a list in Python.

# Beginner Scenario:
# Say you have a list of numbers that you want to clear out and start over.

numbers = [1, 2, 3, 4, 5]

# To clear this list, you can use the clear() method.

numbers.clear()

# After running this line of code, the list will be empty.

print(numbers) # Output: []

# Real World Scenario:
# Say you have a list of shopping items that you want to clear out once you have purchased them.

shopping_list = ["apple", "banana", "carrot"]

# To clear this list, you can use the clear() method.

shopping_list.clear()

# After running this line of code, the list will be empty.

print(shopping_list) # Output: []