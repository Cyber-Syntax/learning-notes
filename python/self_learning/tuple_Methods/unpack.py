# python Unpack Tuples example

# Declaring Tuple, Grup Bildirmek
sample_tuple = (1, 2, 3, 4, 5)

# Unpacking Tuple
bir, second, third, fourth, fifth = sample_tuple

# Printing Values
print("First Value:", bir)
print("Second Value:", second)
print("Third Value:", third)
print("Fourth Value:", fourth)
print("Fifth Value:", fifth)